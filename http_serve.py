"""
This script serves files from the current directory over HTTP on port 8000.
Run it after building the Wasm module.
"""

from http.server import HTTPServer, SimpleHTTPRequestHandler

# Cross-origin headers for COOP and COEP are required for high-resolution clock.
class CORSRequestHandler(SimpleHTTPRequestHandler):
    def end_headers(self):
        self.send_header("Cross-Origin-Opener-Policy", "same-origin")
        self.send_header("Cross-Origin-Embedder-Policy", "require-corp")
        super().end_headers()

server = HTTPServer(("localhost", 8000), CORSRequestHandler)
print("Serving on http://localhost:8000")
server.serve_forever()
