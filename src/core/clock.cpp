Clock::time_point Clock::now() noexcept
{
#ifdef __EMSCRIPTEN__
    // emscripten_get_now() returns milliseconds since the epoch as a double.
    return time_point{std::chrono::microseconds(static_cast<uint64_t>(emscripten_get_now() * 1000.0))};
#else
    return std::chrono::high_resolution_clock::now();
#endif
}

